const dicts: Record<Locale, Dict> = { nl, en }

/** Vertaal een sleutel; vervang {m} e.d. via vars. */
export function t(key: string, vars?: Record<string, string | number>): string {
  let s = dicts[settings.locale]?.[key] ?? nl[key] ?? key
  if (vars) for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, String(vars[k]))
  return s
}

/** Reactieve helper voor in templates: tt('key'). */
export const tt = (key: string, vars?: Record<string, string | number>) =>
  computed(() => t(key, vars))
